    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        int ra = Integer.parseInt(txtRa.getText());
        String name = txtName.getText().toUpperCase();
        String adress = txtAdrress.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        String course = txtCourse.getText();
        
        String sql = "INSERT INTO student (ra, nome, endereco, celular, email, cursos) VALUES(?,?,?,?,?,?)";
        
        PreparedStatement ps = null;
        
        try {
            ps = Conexao.getConexao().prepareStatement(sql);
            ps.setInt(1, ra);
            ps.setString(2, name);
            ps.setString(3, adress);
            ps.setString(4, phone);
            ps.setString(5, email);
            ps.setString(6, course);
            ps.execute();
            ps.close();
            showMessageDialog(null, "Estudante cadastrado com sucesso");
            
        } catch (SQLException ex) {
            showMessageDialog(null, "Falha ao cadastrar estudante");
            Logger.getLogger(StudentDAO.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

